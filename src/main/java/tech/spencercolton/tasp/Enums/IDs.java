    @Getter
    private final int id;

    @Getter
    private final short damage;

    @Getter
    private final List<String> names = new ArrayList<>();

    @Getter
    private final Material material;

    IDs(int id, int damage, Material m, String... names) {
        this.id = id;
        this.damage = (short)damage;
        this.material = m;
        this.names.addAll(Arrays.asList(names));
        this.names.add(this.toString());
        this.names.add(this.material.toString());
    }

    IDs(int id, Material m, String... names) {
        this(id, 0, m, names);
    }

    public static IDs getById(int id) {
        return getByIdDamage(id, 0);
    }

    public static IDs getByName(String name) {
        name = name.toUpperCase();
        name = name.replace(" ", "_");
        name = name.replace("minecraft:", "");
        for(IDs i : values())
            for(String n : i.names)
                if(n.equalsIgnoreCase(name))
                    return i;

        return null;
    }

    public static IDs getByIdDamage(int id, int damage) {
        for (IDs i : values())
            if (i.getId() == id && i.getDamage() == damage)
                return i;
        return null;
    }

    public static IDs getByMaterial(Material m) {
        for(IDs id : values())
            if(id.getMaterial() == m)
                return id;
        assert false;
        return null;
    }

    public static IDs getByIdDamage(String s) {
        Integer damage = null, id = null;

        String[] idDamage = s.split(":");

        try {
            if (idDamage.length == 2)
                damage = Integer.parseInt(idDamage[1]);
            if (idDamage.length >= 1)
                id = Integer.parseInt(idDamage[0]);
        } catch (NumberFormatException e) {
            return null;
        }

        if(damage == null)
            damage = 0;

        assert id != null;

        return getByIdDamage(id, damage);
    }

